    public function getCupSeasons($id)
    {
        try {
            // Guzzle müştərisini yaradın
            $client = new Client();

            // URL-i təyin edin
            $url = "https://football.nowgoal29.com/jsdata/leagueseason/sea$id.js";

            // Sorğu göndərin
            $response = $client->request('GET', $url, [
                'timeout' => 30, // Maksimum vaxt limiti
                'verify' => false, // SSL sertifikatını yoxlamağı söndür
            ]);

            // HTTP status kodunu yoxlayın
            if ($response->getStatusCode() === 200) {
                // Məzmunu əldə edin
                $content = $response->getBody()->getContents();

                // Gələn məlumatı işləyin
                // JavaScript formatını təmizləmək (var ilə başlayan məlumatları çıxarın)
                preg_match("/var\s+arrSeason\s*=\s*(\[[^\]]*\]);/s", $content, $matches);

                // PHP massivinə çevirin
                $encodes = explode(',', preg_replace(['/[\[\]]/'], '', $matches[1]));
                $mapped = array_map(function ($data) {
                    return str_replace("'", '', $data);
                }, $encodes);

                foreach ($mapped as $map) {
                    // Əgər mövcuddursa, əlavə etmə
                    $exists = CupMachSeasons::where('league_id', $id)
                        ->where('title', $map)
                        ->exists();

                    if (!$exists) {
                        CupMachSeasons::create([
                            'league_id' => $id,
                            'title' => $map,
                        ]);
                    }
                }
            }

        } catch (\Exception $e) {
            $this->error("Error fetching data for league ID: {$id} - " . $e->getMessage());
        }
    }
